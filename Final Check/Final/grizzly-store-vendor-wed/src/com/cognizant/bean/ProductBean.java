package com.cognizant.bean;

public class ProductBean {
	
	private int id;
	private String prodId;
	private String prodName;
	private String prodBrand;
	private String prodDesc;
	private String prodCategory;
	private int prodPrice;
	private int prodOffer;
	private int prodRating;
	private int roleId;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getProdId() {
		return prodId;
	}
	public void setProdId(String prodId) {
		this.prodId = prodId;
	}
	public String getProdName() {
		return prodName;
	}
	public void setProdName(String prodName) {
		this.prodName = prodName;
	}
	public String getProdBrand() {
		return prodBrand;
	}
	public void setProdBrand(String prodBrand) {
		this.prodBrand = prodBrand;
	}
	public String getProdDesc() {
		return prodDesc;
	}
	public void setProdDesc(String prodDesc) {
		this.prodDesc = prodDesc;
	}
	public String getProdCategory() {
		return prodCategory;
	}
	public void setProdCategory(String prodCategory) {
		this.prodCategory = prodCategory;
	}
	public int getProdPrice() {
		return prodPrice;
	}
	public void setProdPrice(int prodPrice) {
		this.prodPrice = prodPrice;
	}
	public int getProdOffer() {
		return prodOffer;
	}
	public void setProdOffer(int prodOffer) {
		this.prodOffer = prodOffer;
	}
	public int getProdRating() {
		return prodRating;
	}
	public void setProdRating(int prodRating) {
		this.prodRating = prodRating;
	}
	public int getRoleId() {
		return roleId;
	}
	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}
	
	
	



}